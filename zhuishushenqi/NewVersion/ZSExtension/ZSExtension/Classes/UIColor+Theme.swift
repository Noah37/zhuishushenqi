import Foundation
import UIKit

extension UIColor{
    func daylightTextColor()->UIColor{
        return UIColor.black
    }
    
    func daylightToolBarColor()->UIColor{
        return UIColor.black
    }
    
    func nightCellColor()->UIColor{
        return UIColor.black
    }
    
    func nightSegColor()->UIColor{
        return UIColor.darkGray
    }
    
    func nightNavColor()->UIColor{
        return UIColor.darkGray
    }
    
    
}
